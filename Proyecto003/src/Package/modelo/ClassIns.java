package Package.modelo;

public class ClassIns {
	
	int id;
	String name;

	public ClassIns() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
